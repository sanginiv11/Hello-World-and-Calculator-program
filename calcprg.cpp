#include<iostream>
using namespace std;

int main() {
    int num1, num2, sum=0;
    cout<<"Enter first number: ";
    cin>>num1;
    cout<<"Enter second number: ";
    cin>>num2;

    sum=num1+num2;
    cout<<"Sum of given numbers = "<<sum;

    return 0;
}
